// Offline bake: SVG path data → normalized Shape contours as a committed TS module.
// Runs in node only; SVGLoader is NEVER imported by client code (runtime import
// would cost ~9-12KB gz — measured 2026-07-08).
import { readFileSync, writeFileSync } from "node:fs";
import { JSDOM } from "jsdom";

// SVGLoader.parse needs the browser DOMParser global; Node has none (verified
// fatal on node 24: ReferenceError). jsdom is already a devDependency. The
// polyfill must exist BEFORE SVGLoader loads — static imports hoist above
// assignments, so the loader import must be dynamic.
globalThis.DOMParser = new JSDOM().window.DOMParser;
const { SVGLoader } = await import("three/examples/jsm/loaders/SVGLoader.js");

const SVG_PATH = "assets/monogram-lk.svg";
const OUT_PATH = "src/components/gl/monogram-data.ts";
const CURVE_DIVISIONS = 8; // polygonal lockup → low divisions suffice
const MAX_TOTAL_POINTS = 600; // vertex budget guard (extrusion triples this)

const svg = readFileSync(SVG_PATH, "utf8");
const { paths } = new SVGLoader().parse(svg);

// r185: no-arg toShapes() — the old isCCW arg is gone; hole assignment is
// automatic (even-odd). SVGLoader.createShapes() static is deprecated.
const shapes = paths.flatMap((p) => p.toShapes());
if (shapes.length < 2 || shapes.length > 12) {
  throw new Error(`unexpected shape count ${shapes.length} — check the SVG`);
}

// Collect raw points (SVG y-down), find bounds.
const raw = shapes.map((s) => {
  const { shape, holes } = s.extractPoints(CURVE_DIVISIONS);
  return { shape, holes };
});
let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
for (const { shape, holes } of raw) {
  for (const pt of [...shape, ...holes.flat()]) {
    minX = Math.min(minX, pt.x); maxX = Math.max(maxX, pt.x);
    minY = Math.min(minY, pt.y); maxY = Math.max(maxY, pt.y);
  }
}
const cx = (minX + maxX) / 2;
const cy = (minY + maxY) / 2;
const scale = 2 / (maxY - minY); // height → 2 world units ([-1, 1])

// Normalize: center at origin, flip y (SVG y-down → GL y-up), round to 4dp.
const norm = (pt) => [
  Number(((pt.x - cx) * scale).toFixed(4)),
  Number((-(pt.y - cy) * scale).toFixed(4)),
];
const baked = raw.map(({ shape, holes }) => ({
  points: shape.map(norm),
  holes: holes.map((h) => h.map(norm)),
}));

const total = baked.reduce(
  (n, s) => n + s.points.length + s.holes.reduce((m, h) => m + h.length, 0),
  0,
);
if (total > MAX_TOTAL_POINTS) {
  throw new Error(`${total} contour points > budget ${MAX_TOTAL_POINTS} — simplify the SVG`);
}

const ts = `// GENERATED by scripts/bake-monogram.mjs from assets/monogram-lk.svg — do not edit.
// Regenerate: pnpm bake:monogram
export interface BakedShape {
  readonly points: readonly (readonly [number, number])[];
  readonly holes: readonly (readonly (readonly [number, number])[])[];
}

export const MONOGRAM_SHAPES: readonly BakedShape[] = ${JSON.stringify(baked)};
`;
writeFileSync(OUT_PATH, ts);
console.log(`baked ${baked.length} shapes, ${total} points → ${OUT_PATH}`);
